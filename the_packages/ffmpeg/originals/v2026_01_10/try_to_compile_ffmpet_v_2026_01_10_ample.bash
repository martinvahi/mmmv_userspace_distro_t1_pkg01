    local S_FP_TARGET="$1"
    local S_FP_LINK="$2"
    #----------------------------------------------------------------------
    local S_CMD_LN="ln -s $S_FP_TARGET $S_FP_LINK "
    local SB_DELETE_OLD_LINK="f"
    if [ -e "$S_FP_LINK" ]; then
        if [ ! -h "$S_FP_LINK" ]; then # not a symlink
            echo ""
            echo "The "
            echo ""
            echo -e "\e[31m    $S_FP_LINK \e[39m"
            echo ""
